#include <cstdio>
#include <cstdlib>
#define DEBUG 1

using namespace std;

int main()
{
  char c[19];
  int a;
  while(scanf("%s",c) != EOF){
    c[15]='\0';
    a = atoi(c+10);
    if(a>=120 && a<=218)  printf("SP\n");
    else if(a>=219 && a<=320) printf("SY\n");
    else if(a>=321 && a<=420) printf("BY\n");
    else if(a>=421 && a<=520) printf("JN\n");
    else if(a>=521 && a<=621) printf("SZ\n");
    else if(a>=622 && a<=722) printf("JX\n");
    else if(a>=723 && a<=822) printf("SZZ\n");
    else if(a>=823 && a<=922) printf("CN\n");
    else if(a>=923 && a<=1022) printf("TC\n");
    else if(a>=1023 && a<=1121) printf("TX\n");
    else if(a>=1122 && a<=1221) printf("SS\n");
    else printf("MJ\n");
  }
  return 0;
}
